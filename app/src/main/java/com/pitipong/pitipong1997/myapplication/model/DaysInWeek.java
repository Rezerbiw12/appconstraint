package com.pitipong.pitipong1997.myapplication.model;

public class DaysInWeek {
    String NameInThai;
    String NameInEng;
    int DaysColor;

    public String getNameInThai() {
        return NameInThai;
    }

    public void setNameInThai(String nameInThai) {
        NameInThai = nameInThai;
    }

    public String getNameInEng() {
        return NameInEng;
    }

    public void setNameInEng(String nameInEng) {
        NameInEng = nameInEng;
    }

    public int getDaysColor() {
        return DaysColor;
    }

    public void setDaysColor(int daysColor) {
        DaysColor = daysColor;
    }
}
